from vyperlogix import misc
def list_of_all(lists):
    list0 = misc.copy(lists[0])
    for aList in lists[1:]:
        list0 += misc.copy(aList)
    return list0

def diff(args,arg):
    return set(list_of_all(args))-set(arg)